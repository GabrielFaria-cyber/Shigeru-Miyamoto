class Conteudo {
  final String? titulo;
  final String imagem;
  final String? imagem2;
  final String descricao;
  final String? corFundo;
  final double arredondamento;
  final String? subtitulo;

  Conteudo({
    this.titulo,
    required this.imagem,
    this.imagem2,
    required this.descricao,
    this.corFundo,
    required this.arredondamento,
    this.subtitulo,
  });
}
